package com.aporia.structure;

public class BlockData {

    private int x;
    private int y;
    private int z;

    private String block;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public String getBlock() {
        return block;
    }
}